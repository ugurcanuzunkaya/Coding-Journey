package main

import "fmt"

func main() {
	// heistReady is a boolean variable
	heistReady := false

	// if heistReady is true, print "Let's go!"
	if heistReady {
		fmt.Println("Let's go!")
	} else {
		// if heistReady is false, print "Act normal"
		fmt.Println("Act normal")
	}

	// Output: Act normal

}
